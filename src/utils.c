#include "../philo.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	number;
	int	sign;

	sign = 1;
	number = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -sign;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number *= 10;
		number += str[i] - '0';
		i++;
	}
	return (number * sign);
}

int	ft_isnum(int c)
{
	return (c >= '0' && c <= '9');
}
